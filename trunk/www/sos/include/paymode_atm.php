Cara Pembayaran Melalui Jaringan ATM Prima:
<ul><li>Masukkan PIN</li>
<li>Pilih "Transaksi Lainnya"</li>
<li>Pilih "Transfer"</li>
<li>Pilih "Ke Rek Bank Lain"</li>
<li>Masukkan Kode sandi Bank Permata (013) kemudian tekan "Benar"</li>
<li>Masukkan Jumlah pembayaran sesuai dengan yang ditagihkan dalam tiket.
(Jumlah yang ditransfer harus sama dengan yang ada pada tagihan, tidak lebih dan tidak kurang).
Penting: Jumlah nominal yang tidak sesuai dengan tagihan akan menyebabkan transaksi gagal.</li>
<li>Masukkan Nomor Rekening tujuan kode ban Permata (013) + 16 Digit Kode Bayar, lalu tekan "Benar"</li>
<li>Muncul Layar Konfirmasi Transfer yang berisi nomor rekening tujuan bank permata beserta jumlah yang dibayar, jika sudah benar, Tekan "Benar".</li>
<li>Selesai</li></ul>

Cara Pembayaran Melalui Jaringan ATM Bersama:
<ul><li>Pilih Bahasa</li>
<li>Masukkan PIN</li>
<li>Pilih "Transaksi Lainnya"</li>
<li>Pilih "Transfer"</li>
<li>Pilih "Ke Rekening Bank Lain ATM Bersama/Link"</li>
<li>Masukkan nomor rekening tujuan Kode Bank Permata (013) + 16 Digit Kode Bayar, lalu tekan "Benar"</li>
(Jumlah yang ditransfer harus sama dengan yang ada pada tagihan, tidak lebih dan tidak kurang).
Penting: Jumlah nominal yang tidak sesuai dengan tagihan akan menyebabkan transaksi gagal.</li>
<li>Silakan isi atau kosongkan nomor referensi transfer kemudian tekan "Benar"</li>
<li>Muncul Layar Konfirmasi Transfer yang berisi nomor rekening tujuan bank permata beserta jumlah yang dibayar,
jika sudah benar, Tekan "Benar".</li>
<li>Selesai</li></ul>
